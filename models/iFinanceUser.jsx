
class iFinanceUser {
    constructor(id, name) {

        // This is the primary key of the iFINANCE user info class. Every registered iFINANCE user is assigned a unique user ID.
        // Type String
        this.id = id;

        // The name attribute stores the full name of the iFINANCE user.
        // Type String
        this.name = name;
    }


}

export default iFinanceUser;
